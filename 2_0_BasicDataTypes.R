# Data Type: Logical - TRUE, FALSE
l <- TRUE 
print(class(l))

# Data Type: Numeric - 12.3, 5, 999
n <- 23.5
print(class(n))

# Data Type: Integer - 2L, 34L, 0L
i <- 2L
print(class(i))

# Data Type: Complex - 3 + 2i
c <- 2+5i
print(class(c))

# Data Type: Character - 'a' , '"good", "TRUE", '23.4'
ch <- "TRUE"
print(class(ch))

# Data Type: Raw - "Hello" is stored as 48 65 6c 6c 6f
cTr<- charToRaw("Hello")
print(class(cTr))

